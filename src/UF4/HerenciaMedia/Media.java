package UF4.HerenciaMedia;

public class Media {
    private String nom;
    private int durada;

    public Media () {}
    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getDurada() {
        return durada;
    }

    public void setDurada(int durada) {
        this.durada = durada;
    }
}
